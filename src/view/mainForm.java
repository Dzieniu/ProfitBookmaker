    public void setAllMatches(JList list, ArrayList<Match> matches){
        list.setModel(listOne);
        for(Match match:matches){
            listOne.addElement(match.getName());
        }
        
    }
    
    public void showCourses(){
        int selected = jList1.getSelectedIndex();
        course1Label.setText(String.valueOf(allMatches.get(selected).getCourse1()));
        course2Label.setText(String.valueOf(allMatches.get(selected).getCourseX()));
        course3Label.setText(String.valueOf(allMatches.get(selected).getCourse2()));
    }
    
    public void show100PMatches(JList list, ArrayList<Match> matches){
        list.setModel(listTwo);
        for(Match match:matches) {
            if(match.getWin()==true){
                listTwo.addElement(match.getName());
            }
        }
        if(listTwo.size()==0){
            listTwo.addElement("Niestety, nie ma pewnych meczy");
        }
    }

